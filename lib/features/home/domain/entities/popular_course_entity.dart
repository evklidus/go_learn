class PopularCourseEntity {
  final int id;
  final String imageUrl;
  final String label;
  final int price;
  final double stars;
  final int studentsCount;
  final String title;

  PopularCourseEntity({
    required this.id,
    required this.imageUrl,
    required this.label,
    required this.price,
    required this.stars,
    required this.studentsCount,
    required this.title,
  });
}
